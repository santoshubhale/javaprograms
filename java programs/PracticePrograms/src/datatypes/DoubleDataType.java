package datatypes;

public class DoubleDataType {

	public static void main(String[] args) {
		double d = 10.5d;
		double d1 = 11.5d;
		double sum = d + d1;
		System.out.println("sum:-" + sum);

		System.out.println("--------------");

		double a;
		a = 12.5d;
		double b;
		b = 15.5d;

		double c = a + b;
		System.out.println("Sum:-" + c);

		System.out.println("--------------");

		double x, y, z;
		x = 15.5d;
		y = 20.5d;
		z = 25.5d;
		double sum3 = x + y + z;
		System.out.println("sum:-" + sum3);

	}

}
